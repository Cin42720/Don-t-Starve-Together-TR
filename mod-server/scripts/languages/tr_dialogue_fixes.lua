local function BubbleSafeTurkish(value)
    if type(value) == "string" then
        return value
            :gsub("\196\176", "I")
            :gsub("\196\177", "i")
            :gsub("\197\158", "S")
            :gsub("\197\159", "s")
    end

    if type(value) == "table" then
        for k, v in pairs(value) do
            value[k] = BubbleSafeTurkish(v)
        end
    end

    return value
end

local function ShouldKeepTurkishBubbleKey(key)
    local passthrough_prefix_patterns = {
        "^PIG_",
        "^MERM_",
        "^MERM_KING",
        "^SWAMPIG_",
        "^QUAGMIRE_ELDER_",
    }

    for _, pattern in ipairs(passthrough_prefix_patterns) do
        if key:find(pattern) then
            return true
        end
    end

    return false
end

local function ShouldBubbleSafeKey(key)
    local prefix_patterns = {
        "^HERMITCRAB",
        "^STALKER_ATRIUM",
        "^WAGSTAFF",
        "^SHARKBOI",
        "^PIG_",
        "^DAYWALKER",
        "^SHADOWTHRALL_",
        "^CARNIVAL_HOST_ANNOUNCE",
    }

    local contains_patterns = {
        "_TALK_",
        "_BATTLECRY",
        "_DEATHCRY",
        "_DECAYCRY",
        "_SUMMON_",
        "_USEGATE",
        "_CHANT",
        "_ANNOUNCE_",
    }

    for _, pattern in ipairs(prefix_patterns) do
        if key:find(pattern) then
            return true
        end
    end

    for _, pattern in ipairs(contains_patterns) do
        if key:find(pattern) then
            return true
        end
    end

    return false
end

for key, value in pairs(STRINGS) do
    if type(key) == "string" then
        if ShouldBubbleSafeKey(key) and not ShouldKeepTurkishBubbleKey(key) then
            STRINGS[key] = BubbleSafeTurkish(value)
        end
    end
end

local function BubbleSafeWormwoodSpeech(value)
    if type(value) ~= "string" then
        return value
    end

    return value
        :gsub("Ç", "C")
        :gsub("Ğ", "G")
        :gsub("İ", "I")
        :gsub("Ö", "O")
        :gsub("Ş", "S")
        :gsub("Ü", "U")
        :gsub("ç", "c")
        :gsub("ğ", "g")
        :gsub("ı", "i")
        :gsub("ö", "o")
        :gsub("ş", "s")
        :gsub("ü", "u")
end

AddPrefabPostInit("wormwood", function(inst)
    local previous_speech_override_fn = inst.speech_override_fn

    inst.speech_override_fn = function(instance, message)
        if previous_speech_override_fn ~= nil then
            message = previous_speech_override_fn(instance, message)
        end

        return BubbleSafeWormwoodSpeech(message)
    end
end)

